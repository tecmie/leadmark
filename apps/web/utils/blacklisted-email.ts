export function isEmailBlacklisted(email: string): boolean {
  const emailParts = email.split('@');
  const username = emailParts[0];

  if (!username) {
    return false; // If there's no username, we can't blacklist it
  }

  return emailBlacklist.includes(username.toLowerCase());
}
export function isReservedAddress(username: string): boolean {
  return emailBlacklist.includes(username.toLowerCase());
}
